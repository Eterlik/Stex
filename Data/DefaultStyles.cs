        public List<Style> GetStyleList()
        {
            clearDublicates();
            List<Style> styles = new List<Style>();
            foreach (string defaultStyleName in defaultStyleList)
            {
                styles.Add(new Style() { Name = defaultStyleName });
            }

            Debug.WriteLine("styles: " + styles.Count);
            foreach(Style style in styles)
            {
                Debug.WriteLine(style.Name);
            }
            return styles;
        }

        private void clearDublicates()
        {
            List<string> noDublicates = new List<string>();

            for (int i = 0; defaultStyleList.Count > i; i++)
            {
                if (!noDublicates.Contains(defaultStyleList[i]))
                {
                    noDublicates.Add(defaultStyleList[i]);
                }
            }
            defaultStyleList = noDublicates;
        }
